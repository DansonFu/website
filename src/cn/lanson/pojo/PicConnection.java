package cn.lanson.pojo;

public class PicConnection {
    private Integer id;

    private Integer pid;

    private Integer piclist;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }

    public Integer getPiclist() {
        return piclist;
    }

    public void setPiclist(Integer piclist) {
        this.piclist = piclist;
    }
}